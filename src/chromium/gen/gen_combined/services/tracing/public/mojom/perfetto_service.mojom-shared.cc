std::ostream& operator<<(std::ostream& os, TracingClientPriority value) {
  switch(value) {
    case TracingClientPriority::kUnknown:
      return os << "TracingClientPriority::kUnknown";
    case TracingClientPriority::kBackground:
      return os << "TracingClientPriority::kBackground";
    case TracingClientPriority::kUserInitiated:
      return os << "TracingClientPriority::kUserInitiated";
    default:
      return os << "Unknown TracingClientPriority value: " << static_cast<int32_t>(value);
  }
}

