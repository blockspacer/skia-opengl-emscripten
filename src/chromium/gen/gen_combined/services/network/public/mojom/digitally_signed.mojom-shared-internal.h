#pragma pack(push, 1)
class  DigitallySigned_Data {
 public:
  class BufferWriter {
   public:
    BufferWriter() = default;

    void Allocate(mojo::internal::Buffer* serialization_buffer) {
      serialization_buffer_ = serialization_buffer;
      index_ = serialization_buffer_->Allocate(sizeof(DigitallySigned_Data));
      new (data()) DigitallySigned_Data();
    }

    bool is_null() const { return !serialization_buffer_; }
    DigitallySigned_Data* data() {
      DCHECK(!is_null());
      return serialization_buffer_->Get<DigitallySigned_Data>(index_);
    }
    DigitallySigned_Data* operator->() { return data(); }

   private:
    mojo::internal::Buffer* serialization_buffer_ = nullptr;
    size_t index_ = 0;

    DISALLOW_COPY_AND_ASSIGN(BufferWriter);
  };

  static bool Validate(const void* data,
                       mojo::internal::ValidationContext* validation_context);

  mojo::internal::StructHeader header_;
  int32_t hash_algorithm;
  int32_t signature_algorithm;
  mojo::internal::Pointer<mojo::internal::Array_Data<uint8_t>> signature;

 private:
  DigitallySigned_Data();
  ~DigitallySigned_Data() = delete;
};
static_assert(sizeof(DigitallySigned_Data) == 24,
              "Bad sizeof(DigitallySigned_Data)");
// Used by DigitallySigned::WrapAsMessage to lazily serialize the struct.
template <typename UserType, typename DataView>
struct DigitallySigned_UnserializedMessageContext
    : public mojo::internal::UnserializedMessageContext {
 public:
  static const mojo::internal::UnserializedMessageContext::Tag kMessageTag;

  DigitallySigned_UnserializedMessageContext(
    uint32_t message_name,
    uint32_t message_flags,
    UserType input)
      : mojo::internal::UnserializedMessageContext(&kMessageTag, message_name, message_flags)
      , user_data_(std::move(input)) {}
  ~DigitallySigned_UnserializedMessageContext() override = default;

  UserType TakeData() {
    return std::move(user_data_);
  }

 private:
  // mojo::internal::UnserializedMessageContext:
  void Serialize(mojo::internal::SerializationContext* context,
                 mojo::internal::Buffer* buffer) override {
    DigitallySigned_Data::BufferWriter writer;
    mojo::internal::Serialize<DataView>(user_data_, buffer, &writer, context);
  }

  UserType user_data_;
};

template <typename UserType, typename DataView>
const mojo::internal::UnserializedMessageContext::Tag
    DigitallySigned_UnserializedMessageContext<UserType, DataView>::kMessageTag = {};

