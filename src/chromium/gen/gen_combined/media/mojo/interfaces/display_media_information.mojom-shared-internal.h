#pragma pack(push, 1)
class  DisplayMediaInformation_Data {
 public:
  class BufferWriter {
   public:
    BufferWriter() = default;

    void Allocate(mojo::internal::Buffer* serialization_buffer) {
      serialization_buffer_ = serialization_buffer;
      index_ = serialization_buffer_->Allocate(sizeof(DisplayMediaInformation_Data));
      new (data()) DisplayMediaInformation_Data();
    }

    bool is_null() const { return !serialization_buffer_; }
    DisplayMediaInformation_Data* data() {
      DCHECK(!is_null());
      return serialization_buffer_->Get<DisplayMediaInformation_Data>(index_);
    }
    DisplayMediaInformation_Data* operator->() { return data(); }

   private:
    mojo::internal::Buffer* serialization_buffer_ = nullptr;
    size_t index_ = 0;

    DISALLOW_COPY_AND_ASSIGN(BufferWriter);
  };

  static bool Validate(const void* data,
                       mojo::internal::ValidationContext* validation_context);

  mojo::internal::StructHeader header_;
  int32_t display_surface;
  uint8_t logical_surface : 1;
  uint8_t pad1_[3];
  int32_t cursor;
  uint8_t padfinal_[4];

 private:
  DisplayMediaInformation_Data();
  ~DisplayMediaInformation_Data() = delete;
};
