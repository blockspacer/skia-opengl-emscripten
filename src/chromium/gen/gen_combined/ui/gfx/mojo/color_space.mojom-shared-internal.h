#pragma pack(push, 1)
class  ColorSpace_Data {
 public:
  class BufferWriter {
   public:
    BufferWriter() = default;

    void Allocate(mojo::internal::Buffer* serialization_buffer) {
      serialization_buffer_ = serialization_buffer;
      index_ = serialization_buffer_->Allocate(sizeof(ColorSpace_Data));
      new (data()) ColorSpace_Data();
    }

    bool is_null() const { return !serialization_buffer_; }
    ColorSpace_Data* data() {
      DCHECK(!is_null());
      return serialization_buffer_->Get<ColorSpace_Data>(index_);
    }
    ColorSpace_Data* operator->() { return data(); }

   private:
    mojo::internal::Buffer* serialization_buffer_ = nullptr;
    size_t index_ = 0;

    DISALLOW_COPY_AND_ASSIGN(BufferWriter);
  };

  static bool Validate(const void* data,
                       mojo::internal::ValidationContext* validation_context);

  mojo::internal::StructHeader header_;
  int32_t primaries;
  int32_t transfer;
  int32_t matrix;
  int32_t range;
  mojo::internal::Pointer<mojo::internal::Array_Data<float>> custom_primary_matrix;
  mojo::internal::Pointer<mojo::internal::Array_Data<float>> custom_transfer_params;

 private:
  ColorSpace_Data();
  ~ColorSpace_Data() = delete;
};
static_assert(sizeof(ColorSpace_Data) == 40,
              "Bad sizeof(ColorSpace_Data)");
// Used by ColorSpace::WrapAsMessage to lazily serialize the struct.
template <typename UserType, typename DataView>
struct ColorSpace_UnserializedMessageContext
    : public mojo::internal::UnserializedMessageContext {
 public:
  static const mojo::internal::UnserializedMessageContext::Tag kMessageTag;

  ColorSpace_UnserializedMessageContext(
    uint32_t message_name,
    uint32_t message_flags,
    UserType input)
      : mojo::internal::UnserializedMessageContext(&kMessageTag, message_name, message_flags)
      , user_data_(std::move(input)) {}
  ~ColorSpace_UnserializedMessageContext() override = default;

  UserType TakeData() {
    return std::move(user_data_);
  }

 private:
  // mojo::internal::UnserializedMessageContext:
  void Serialize(mojo::internal::SerializationContext* context,
                 mojo::internal::Buffer* buffer) override {
    ColorSpace_Data::BufferWriter writer;
    mojo::internal::Serialize<DataView>(user_data_, buffer, &writer, context);
  }

  UserType user_data_;
};

template <typename UserType, typename DataView>
const mojo::internal::UnserializedMessageContext::Tag
    ColorSpace_UnserializedMessageContext<UserType, DataView>::kMessageTag = {};

