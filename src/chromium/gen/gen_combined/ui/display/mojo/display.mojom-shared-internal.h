#pragma pack(push, 1)
class  Display_Data {
 public:
  class BufferWriter {
   public:
    BufferWriter() = default;

    void Allocate(mojo::internal::Buffer* serialization_buffer) {
      serialization_buffer_ = serialization_buffer;
      index_ = serialization_buffer_->Allocate(sizeof(Display_Data));
      new (data()) Display_Data();
    }

    bool is_null() const { return !serialization_buffer_; }
    Display_Data* data() {
      DCHECK(!is_null());
      return serialization_buffer_->Get<Display_Data>(index_);
    }
    Display_Data* operator->() { return data(); }

   private:
    mojo::internal::Buffer* serialization_buffer_ = nullptr;
    size_t index_ = 0;

    DISALLOW_COPY_AND_ASSIGN(BufferWriter);
  };

  static bool Validate(const void* data,
                       mojo::internal::ValidationContext* validation_context);

  mojo::internal::StructHeader header_;
  int64_t id;
  mojo::internal::Pointer<::gfx::mojom::internal::Rect_Data> bounds;
  mojo::internal::Pointer<::gfx::mojom::internal::Size_Data> size_in_pixels;
  mojo::internal::Pointer<::gfx::mojom::internal::Rect_Data> work_area;
  float device_scale_factor;
  int32_t rotation;
  int32_t touch_support;
  int32_t accelerometer_support;
  mojo::internal::Pointer<::gfx::mojom::internal::Size_Data> maximum_cursor_size;
  int32_t color_depth;
  int32_t depth_per_component;
  uint8_t is_monochrome : 1;
  uint8_t padfinal_[7];

 private:
  Display_Data();
  ~Display_Data() = delete;
};
static_assert(sizeof(Display_Data) == 80,
              "Bad sizeof(Display_Data)");
// Used by Display::WrapAsMessage to lazily serialize the struct.
template <typename UserType, typename DataView>
struct Display_UnserializedMessageContext
    : public mojo::internal::UnserializedMessageContext {
 public:
  static const mojo::internal::UnserializedMessageContext::Tag kMessageTag;

  Display_UnserializedMessageContext(
    uint32_t message_name,
    uint32_t message_flags,
    UserType input)
      : mojo::internal::UnserializedMessageContext(&kMessageTag, message_name, message_flags)
      , user_data_(std::move(input)) {}
  ~Display_UnserializedMessageContext() override = default;

  UserType TakeData() {
    return std::move(user_data_);
  }

 private:
  // mojo::internal::UnserializedMessageContext:
  void Serialize(mojo::internal::SerializationContext* context,
                 mojo::internal::Buffer* buffer) override {
    Display_Data::BufferWriter writer;
    mojo::internal::Serialize<DataView>(user_data_, buffer, &writer, context);
  }

  UserType user_data_;
};

template <typename UserType, typename DataView>
const mojo::internal::UnserializedMessageContext::Tag
    Display_UnserializedMessageContext<UserType, DataView>::kMessageTag = {};

