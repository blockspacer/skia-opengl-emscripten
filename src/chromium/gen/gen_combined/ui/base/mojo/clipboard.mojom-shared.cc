std::ostream& operator<<(std::ostream& os, ClipboardType value) {
  switch(value) {
    case ClipboardType::COPY_PASTE:
      return os << "ClipboardType::COPY_PASTE";
    case ClipboardType::SELECTION:
      return os << "ClipboardType::SELECTION";
    case ClipboardType::DRAG:
      return os << "ClipboardType::DRAG";
    default:
      return os << "Unknown ClipboardType value: " << static_cast<int32_t>(value);
  }
}

