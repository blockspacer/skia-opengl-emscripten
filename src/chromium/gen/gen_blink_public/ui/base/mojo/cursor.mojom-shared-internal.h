#pragma pack(push, 1)
class  Cursor_Data {
 public:
  class BufferWriter {
   public:
    BufferWriter() = default;

    void Allocate(mojo::internal::Buffer* serialization_buffer) {
      serialization_buffer_ = serialization_buffer;
      index_ = serialization_buffer_->Allocate(sizeof(Cursor_Data));
      new (data()) Cursor_Data();
    }

    bool is_null() const { return !serialization_buffer_; }
    Cursor_Data* data() {
      DCHECK(!is_null());
      return serialization_buffer_->Get<Cursor_Data>(index_);
    }
    Cursor_Data* operator->() { return data(); }

   private:
    mojo::internal::Buffer* serialization_buffer_ = nullptr;
    size_t index_ = 0;

    DISALLOW_COPY_AND_ASSIGN(BufferWriter);
  };

  static bool Validate(const void* data,
                       mojo::internal::ValidationContext* validation_context);

  mojo::internal::StructHeader header_;
  int32_t native_type;
  float device_scale_factor;
  mojo::internal::Pointer<::gfx::mojom::internal::Point_Data> hotspot;
  mojo::internal::Pointer<::skia::mojom::internal::Bitmap_Data> bitmap;

 private:
  Cursor_Data();
  ~Cursor_Data() = delete;
};
static_assert(sizeof(Cursor_Data) == 32,
              "Bad sizeof(Cursor_Data)");
// Used by Cursor::WrapAsMessage to lazily serialize the struct.
template <typename UserType, typename DataView>
struct Cursor_UnserializedMessageContext
    : public mojo::internal::UnserializedMessageContext {
 public:
  static const mojo::internal::UnserializedMessageContext::Tag kMessageTag;

  Cursor_UnserializedMessageContext(
    uint32_t message_name,
    uint32_t message_flags,
    UserType input)
      : mojo::internal::UnserializedMessageContext(&kMessageTag, message_name, message_flags)
      , user_data_(std::move(input)) {}
  ~Cursor_UnserializedMessageContext() override = default;

  UserType TakeData() {
    return std::move(user_data_);
  }

 private:
  // mojo::internal::UnserializedMessageContext:
  void Serialize(mojo::internal::SerializationContext* context,
                 mojo::internal::Buffer* buffer) override {
    Cursor_Data::BufferWriter writer;
    mojo::internal::Serialize<DataView>(user_data_, buffer, &writer, context);
  }

  UserType user_data_;
};

template <typename UserType, typename DataView>
const mojo::internal::UnserializedMessageContext::Tag
    Cursor_UnserializedMessageContext<UserType, DataView>::kMessageTag = {};

