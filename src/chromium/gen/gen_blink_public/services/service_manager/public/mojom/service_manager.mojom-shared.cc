std::ostream& operator<<(std::ostream& os, InstanceState value) {
  switch(value) {
    case InstanceState::kCreated:
      return os << "InstanceState::kCreated";
    case InstanceState::kStarted:
      return os << "InstanceState::kStarted";
    case InstanceState::kUnreachable:
      return os << "InstanceState::kUnreachable";
    default:
      return os << "Unknown InstanceState value: " << static_cast<int32_t>(value);
  }
}

