std::ostream& operator<<(std::ostream& os, AudioOutputStreamObserver_DisconnectReason value) {
  switch(value) {
    case AudioOutputStreamObserver_DisconnectReason::kDefault:
      return os << "AudioOutputStreamObserver_DisconnectReason::kDefault";
    case AudioOutputStreamObserver_DisconnectReason::kPlatformError:
      return os << "AudioOutputStreamObserver_DisconnectReason::kPlatformError";
    case AudioOutputStreamObserver_DisconnectReason::kTerminatedByClient:
      return os << "AudioOutputStreamObserver_DisconnectReason::kTerminatedByClient";
    case AudioOutputStreamObserver_DisconnectReason::kStreamCreationFailed:
      return os << "AudioOutputStreamObserver_DisconnectReason::kStreamCreationFailed";
    case AudioOutputStreamObserver_DisconnectReason::kDocumentDestroyed:
      return os << "AudioOutputStreamObserver_DisconnectReason::kDocumentDestroyed";
    default:
      return os << "Unknown AudioOutputStreamObserver_DisconnectReason value: " << static_cast<int32_t>(value);
  }
}

